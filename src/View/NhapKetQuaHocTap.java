    private void jbtThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtThemActionPerformed
        try {
            String maSV = jtfMaSV.getText();
            Double diemTX1 = Double.parseDouble(jtfDiemTX1.getText());
            Double diemTX2 = Double.parseDouble(jtfDiemTX2.getText());
            Double diemHS2 = Double.parseDouble(jtfDiemHS2.getText());
            int soTietNghi = Integer.parseInt(jtfSoTietNghi.getText());
            if (maSV.equals("") || jtfDiemTX1.getText().equals("") || jtfDiemTX2.getText().equals("") || jtfDiemHS2.getText().equals("") || jtfSoTietNghi.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Dữ liệu khoogn để trônghs", "Lỗi", JOptionPane.CANCEL_OPTION);
                throw new Exception();
            }
            if (diemTX1 > 10 || diemTX1 < 0 || diemHS2 > 10 || diemHS2 < 0 || diemTX2 > 10 || diemTX2 < 0) {
                JOptionPane.showMessageDialog(this, "Điểm không hợp lệ", "Lỗi", JOptionPane.CANCEL_OPTION);
                throw new Exception();
            }
            KetQuaHocTap s = new KetQuaHocTap();
            s.setMaSV(maSV);
            s.setMaMon(jcbMaMH.getSelectedItem().toString());
            s.setHocKy(KetQuaController.getHocKy(jcbMaMH.getSelectedItem().toString()));
            s.setDiemTX1(diemTX1);
            s.setDdiemTX2(diemTX2);
            s.setDiemGiuaKy(diemHS2);
            Double dtb = (Double.parseDouble(jtfDiemTX1.getText()) + Double.parseDouble(jtfDiemTX2.getText()) + Double.parseDouble(jtfDiemHS2.getText()) * 2) / 4;
            s.setDiemTrungBinh(dtb);
            s.setSoTietNghi(soTietNghi);
            s.setDieuKienDuThi(jcbDKDuThi.getSelectedItem().toString());
            s.setDiemThi(null);
            s.setDiemChu(null);
            s.setDiemTK10(null);
            s.setDiemTK4(null);
            s.setXepLoai(null);
            s.setTenSV(jtfTenSV.getText());
            s.setTenMon(jtfTenMonHoc.getText().toString());
            s.setMaLop(null);
            s.setTenLop(null);
            KetQuaController.setDiemTrenLop(s);
            jcbMaMHActionPerformed(evt);
            jlbTrangThai.setText("Thêm thành công!");
            jlbTrangThai.setForeground(Color.green);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString(), "Lỗi", JOptionPane.CANCEL_OPTION);
            jlbTrangThai.setText("Thêm thất bại!");
            jlbTrangThai.setForeground(Color.red);
        }
    }//GEN-LAST:event_jbtThemActionPerformed

