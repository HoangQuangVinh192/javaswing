    private void jbtXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtXoaActionPerformed
        try {
            String masv = jtfMaSV.getText().trim();
            String mamh = jcbMaMH.getSelectedItem().toString();
            if (jtfTenMonHoc.getText().trim().equals("")) {
                throw new Exception("Không để trống mã môn học");
            }
            if (masv.equals("")) {
                throw new Exception("Không để trống mã sinh viên");
            }

            int click = JOptionPane.showConfirmDialog(null, "Xóa sinh viên mã: " + masv + "?", "Xóa?", JOptionPane.YES_NO_OPTION);
            if (click == JOptionPane.YES_OPTION) {
                KetQuaController.delete(mamh, masv);
                jlbTrangThai.setText("Xóa sinh viên thành công");
                jlbTrangThai.setForeground(Color.green);
                jcbMaMHActionPerformed(evt);
            } else {
                throw new Exception();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString(), "Lỗi", JOptionPane.CANCEL_OPTION);
            jlbTrangThai.setText("Sửa thất bại!");
            jlbTrangThai.setForeground(Color.red);
        }
    }//GEN-LAST:event_jbtXoaActionPerformed

