    private void jcbMaMHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbMaMHActionPerformed
        String ma = jcbMaMH.getSelectedItem().toString();
        jtfTenMonHoc.setText(KetQuaController.getTenMonHoc(ma));
        ArrayList<KetQuaHocTap> list = KetQuaController.getSVTheoMonHoc(ma);
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("STT");
        model.addColumn("Mã sinh viên");
        model.addColumn("Họ tên");
        model.addColumn("Điểm TX1");
        model.addColumn("Điểm TX2");
        model.addColumn("Điểm HS2");
        model.addColumn("Điểm TB");
        model.addColumn("Số tiết nghỉ");
        model.addColumn("ĐK dự thi");

        for (int i = 0; i < list.size(); i++) {
            model.addRow(new Object[]{
                (i + 1), list.get(i).getMaSV(), list.get(i).getTenSV(), list.get(i).getDiemTX1(), list.get(i).getDdiemTX2(),
                list.get(i).getDiemGiuaKy(), list.get(i).getDiemTrungBinh(), list.get(i).getSoTietNghi(),
                list.get(i).getDieuKienDuThi()
            });
        }
        jTable1.setModel(model);
    }//GEN-LAST:event_jcbMaMHActionPerformed

