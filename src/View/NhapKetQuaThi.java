    private void jbtThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtThemActionPerformed
        try {
            String maSV = jtfMaSV.getText();
            Double diemThi = Double.parseDouble(jtfDiemThi.getText());
            String maMH = jcbMaMH.getSelectedItem().toString();
            if (maSV.equals("") || jtfDiemThi.getText().equals("") || jtfTenMH.getText().equals("")) {
                throw new Exception("Nhập đầy đủ dữ liệu");
            }
            KetQuaHocTap s = KetQuaController.getSV(maSV, maMH);
            if (s.getDieuKienDuThi().equals("Không đủ điều kiện")) {
                throw new Exception("Sinh viên không đủ điều kiện dự thi");
            }
            if (s == null) {
                throw new Exception("Sinh viên chưa học môn này");
            }
            Double diemTK10 = (s.getDiemGiuaKy() + diemThi * 2) / 3;
            s.setDiemTK10(diemTK10);
            String xepLoai, dChu;
            Double diemTK4;
            if (diemThi < 0 || diemThi > 10) {
                throw new Exception("Điểm thi không hợp lệ");
            }
            if (diemTK10 < 4) {
                xepLoai = "Kém";
                dChu = "F";
                diemTK4 = 0.0;
            } else if (diemTK10 < 4.7) {
                xepLoai = "Trung bình yếu";
                dChu = "D";
                diemTK4 = 1.0;
            } else if (diemTK10 < 5.5) {
                xepLoai = "Trung bình yếu";
                dChu = "D+";
                diemTK4 = 1.5;
            } else if (diemTK10 < 6.2) {
                xepLoai = "Trung bình";
                dChu = "C";
                diemTK4 = 2.0;
            } else if (diemTK10 < 7.0) {
                xepLoai = "Trung bình";
                dChu = "C+";
                diemTK4 = 2.5;
            } else if (diemTK10 < 7.7) {
                xepLoai = "Khá";
                dChu = "B";
                diemTK4 = 3.0;
            } else if (diemTK10 < 8.5) {
                xepLoai = "Khá";
                dChu = "B+";
                diemTK4 = 3.5;
            } else {
                xepLoai = "Giỏi";
                dChu = "A";
                diemTK4 = 4.0;
            }
            s.setDiemThi(diemThi);
            s.setDiemTK10(diemTK10);
            s.setDiemTK4(diemTK4);
            s.setDiemChu(dChu);
            s.setXepLoai(xepLoai);
            KetQuaController.insertDiemThi(s);
            jcbMaMHActionPerformed(evt);
            jlbTrangThai.setText("Thêm thành công!");
            jlbTrangThai.setForeground(Color.green);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
            jlbTrangThai.setText("Thêm thất bại!");
            jlbTrangThai.setForeground(Color.red);
        }
    }//GEN-LAST:event_jbtThemActionPerformed

