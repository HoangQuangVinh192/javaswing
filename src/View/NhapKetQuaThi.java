    private void jcbMaMHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbMaMHActionPerformed
        String ma = jcbMaMH.getSelectedItem().toString();
        jtfTenMH.setText(KetQuaController.getTenMonHoc(ma));
        ArrayList<KetQuaHocTap> list = KetQuaController.getSVTheoMonHoc(ma);
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("STT");
        model.addColumn("Mã sinh viên");
        model.addColumn("Họ tên");
        model.addColumn("Điểm TB trên lớp");
        model.addColumn("Điểm Thi");
        model.addColumn("Điểm tổng kết 10");
        model.addColumn("Điểm tổng kết 4");
        model.addColumn("Điểm chữ");
        model.addColumn("Xếp loại");
        model.addColumn("Ghi chú");
        for (int i = 0; i < list.size(); i++) {
            String dthi, d10, d4;
            if (list.get(i).getDiemThi() < 0) {
                dthi = "";
            } else {
                dthi = (String) (list.get(i).getDiemThi().toString());
            }
            ///////////////
            if (list.get(i).getDiemTK10() < 0) {
                d10 = "";
            } else {
                d10 = (String) (list.get(i).getDiemTK10().toString());
            }
            /////////////

            if (list.get(i).getDiemTK4() < 0) {
                d4 = "";
            } else {
                d4 = (String) (list.get(i).getDiemTK4().toString());
            }
            if (list.get(i).getDieuKienDuThi().equals("Không đủ điều kiện")) {
                model.addRow(new Object[]{
                    (i + 1), list.get(i).getMaSV(), list.get(i).getTenSV(), list.get(i).getDiemTrungBinh(),
                    "x", "x", "x",
                    "x", "x", "Học lại"
                });
            } else if (list.get(i).getDiemTK10()<4 && !d10.equals("")) {
                model.addRow(new Object[]{
                    (i + 1), list.get(i).getMaSV(), list.get(i).getTenSV(), list.get(i).getDiemTrungBinh(),
                    dthi, d10, d4,
                    list.get(i).getDiemChu(), list.get(i).getXepLoai(), "Học lại"
                });
            }
            else{
                    model.addRow(new Object[]{
                    (i + 1), list.get(i).getMaSV(), list.get(i).getTenSV(), list.get(i).getDiemTrungBinh(),
                    dthi, d10, d4,
                    list.get(i).getDiemChu(), list.get(i).getXepLoai(), ""
                });
            }
        }
        jTable1.setModel(model);
    }//GEN-LAST:event_jcbMaMHActionPerformed

