    public void hienThi() {
        ArrayList<TaiKhoan> list = taikhoancontroller.getAll();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("Tên tài khoản");
        model.addColumn("Mật khẩu");
        model.addColumn("quyền truy cập");
        model.addColumn("mã sinh viên");
        for (int i = 0; i < list.size(); i++) {
            model.addRow(new Object[]{
                list.get(i).getTenTaiKhoan(), list.get(i).getMatKhau(), list.get(i).getQuyenTruyCap(),list.get(i).getMasinhvien()
            });
        }
        jtbtaikhoan.setModel(model);
    }
