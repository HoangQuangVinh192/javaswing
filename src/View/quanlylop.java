    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:

        try {
            //lấy dòng chọn trong table
            if (txtmalop.getText().trim().equals("") || txttenlop.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "bạn phải nhập đẩy đủ thông tin");
            } else {
                lophoc lh = new lophoc();
                lh.setMaLop(txtmalop.getText().trim());
                lh.setTenLop(txttenlop.getText().trim());
                lh.setMaKhoa(cbmakhoa.getSelectedItem().toString().trim());
                boolean tb = db.Add(lh);
                if (tb) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "thêm thất bại");
                }
                //lấy lại dữ liệu cho bảng
                hienThi();
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Không sửa được " + ex.toString());
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        try {
            //lấy dòng chọn trong table
            int row = jtblop.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(rootPane, "bạn cần chọn một dòng để sửa");
            } else {
                lophoc lh = new lophoc();
                lh.setMaLop(txtmalop.getText().trim());
                lh.setTenLop(txttenlop.getText().trim());
                lh.setMaKhoa(cbmakhoa.getSelectedItem().toString().trim());
                boolean tb = db.update(lh);
                if (tb) {
                    JOptionPane.showMessageDialog(rootPane, "sửa thành công");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "sửa thất bại");
                }
                //lấy lại dữ liệu cho bảng
                hienThi();
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Không sửa được " + ex.toString());
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        try {
            int click = JOptionPane.showConfirmDialog(null, "Chắc chắn môn học này", "Xóa?", JOptionPane.YES_NO_OPTION);
            if (click == JOptionPane.YES_OPTION) {
                //lấy dòng chọn hiện thời trong table
                int row = jtblop.getSelectedRow();
                //lấy số báo danh ở cột 0
                String malop = jtblop.getValueAt(row, 0).toString();
                //truyền vào phognf thi xóa theo sbd
                lophoc lh = getlopDetail();
                db.delete(lh);
                hienThi();
                clearTextDetail();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Không xóa được " + ex.toString());
        }
    }//GEN-LAST:event_btnxoaActionPerformed

