    private lophoc getlopDetail() {
        String Malop = txtmalop.getText().trim();
        String Telop = txttenlop.getText().trim();
        String makhoa = cbmakhoa.getSelectedItem().toString().trim();
        return new lophoc(Malop, Telop, makhoa);
    }

    public void clearTextDetail() {

        txttenlop.setText("");
        txtmalop.setText("");

    }

    public void hienThi() {
        ArrayList<lophoc> list = lopcontroller.getAll();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("Mã Lớp");
        model.addColumn("Tên Lớp");
        model.addColumn("Mã Khoa");
        for (int i = 0; i < list.size(); i++) {
            model.addRow(new Object[]{
                list.get(i).getMaLop(), list.get(i).getTenLop(), list.get(i).getMaKhoa()
            });
        }
        jtblop.setModel(model);
    }

    public void laykhoa() {
        ArrayList<Khoa> list = lopcontroller.getkhoa();
        for (int i = 0; i < list.size(); i++) {
            cbmakhoa.addItem(list.get(i).getMaKhoa());
        }
    }

